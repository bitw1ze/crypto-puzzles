from p3 import xor_byte_bruteforce
from p5 import xor_repeat_cipher
from base64 import b64decode
import sys

def hamming_distance(msg1, msg2):
  if len(msg1) != len(msg2):
    raise Exception("Arguments must be equal length!")

  return sum([on_bits(m ^ n) for m, n in zip(msg1, msg2)])

def on_bits(n):
  c = 0
  while n > 0:
    n &= n - 1
    c += 1
  return c

def find_keysize(ct, min_key=2, max_key=40, blockpairs=10):
  keysize, norm_dist = 0, 999999999999
  for ks in range(min_key, max_key + 1):
    nd = 0
    for i in range(blockpairs):
      m1, m2 = ct[i*ks : (i+1)*ks], ct[(i+1)*ks : (i+2)*ks]
      nd += hamming_distance(m1, m2) / ks
    if nd < norm_dist:
       keysize, norm_dist = ks, nd
    
  return keysize

def _xor_repeat_bruteforce(ct, keysize):
  key = []
  for i in range(keysize):
    block = bytearray([ct[j] for j in range(i, len(ct), keysize)])
    _, _, k = xor_byte_bruteforce(block)
    key.append(k)
  return xor_repeat_cipher(ct, bytearray(key))

def xor_repeat_bruteforce(ct):
  return _xor_repeat_bruteforce(ct, find_keysize(ct))

